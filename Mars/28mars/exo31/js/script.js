// Créer une var de type NUMBER avec la valeur 10
/*
    Opérateur d'affectation
*/ 
var myNumber = 10;
console.log(myNumber);

//  Opérateur d'incrémentation : ++
myNumber++;
console.log(myNumber);

//  Opérateur décrémentation : --
myNumber--;
console.log(myNumber);

//  Opérateur d'addition/concaténation : +

var myAdd = 7 + 5;
console.log(myAdd);

var myConc = 'Salut ' + 'mon ' + 'pote';
console.log(myConc);

// Opérateur de soustraction : -
var mySous = 7 - 5;
console.log(mySous);

// Opérateur de multiplication : *
var myMult = 7 * 5;
console.log(myMult);

// Opératuer de division : /
var myDivi = 14 / 3;
console.log(myDivi);

// Opérateur modulo : %
var myModulo = 12 % 5;
console.log(myModulo);

// Opérateur d'addition et d'affectation : +=

var first = 10;
var second = 5;

// first = first + second;
first += second;
console.log(first);

// Opérateur de spustraction et d'affectation : -=

second -= first;
console.log(second);